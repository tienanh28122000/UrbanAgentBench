def _fetch_ptv_departures(stop_lat: float, stop_lng: float, dest_name: str) -> list:
    """Fetch real schedules from PTV API if key is available. Returns [] otherwise."""
    if not PTV_DEV_ID or not PTV_API_KEY:
        return []
    import hashlib, hmac, urllib.parse
    # PTV uses HMAC-SHA1 signature
    base_url = "/v3/stops/location/{lat},{lng}?max_results=1".format(lat=stop_lat, lng=stop_lng)
    raw = f"{base_url}\n" if "\n" not in base_url else base_url
    signature = hmac.new(
        PTV_API_KEY.encode("utf-8"), (base_url + f"?devid={PTV_DEV_ID}").encode("utf-8"),
        hashlib.sha1
    ).hexdigest().upper()
    url = f"https://timetableapi.ptv.vic.gov.au{base_url}&devid={PTV_DEV_ID}&signature={signature}"
    try:
        resp = requests.get(url, timeout=10)
        if resp.status_code == 200:
            stops = resp.json().get("stops", [])
            if stops:
                stop_id = stops[0]["stop_id"]
                route_type = stops[0].get("route_type", 1)
                # Fetch departures for this stop
                dep_url = f"/v3/departures/route_type/{route_type}/stop/{stop_id}?max_results=5&devid={PTV_DEV_ID}"
                sig2 = hmac.new(
                    PTV_API_KEY.encode("utf-8"), dep_url.encode("utf-8"), hashlib.sha1
                ).hexdigest().upper()
                full_dep_url = f"https://timetableapi.ptv.vic.gov.au{dep_url}&signature={sig2}"
                dep_resp = requests.get(full_dep_url, timeout=10)
                if dep_resp.status_code == 200:
                    deps = dep_resp.json().get("departures", [])
                    results = []
                    for d in deps[:5]:
                        sched = d.get("scheduled_departure_utc", "")
                        if sched:
                            dt = datetime.fromisoformat(sched.replace("Z", "+00:00"))
                            local_time = dt.strftime("%H:%M")
                        else:
                            local_time = "N/A"
                        results.append({
                            "route_number": str(d.get("route_id", "?")),
                            "destination": dest_name,
                            "departure_time": local_time,
                            "source": "PTV"
                        })
                    if results:
                        print("  -> Successfully fetched real schedule from PTV API.")
                        return results
    except Exception as e:
        print(f"  [~] PTV API error: {e}, falling back to fake data.")
    return []


