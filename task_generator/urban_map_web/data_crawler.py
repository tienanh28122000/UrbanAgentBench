def build_database(min_webpages: int = MIN_WEBPAGES, max_crawls: int = MAX_CRAWLS):
    if not API_KEY:
        print("Please set the GOOGLE_MAPS_API_KEY environment variable before running.")
        return

    # Load datasets only once
    all_names = load_name_dataset()
    all_landmarks = load_landmarks()

    # Load places cache (pre-generated by place_crawler.py)
    places_cache = load_places_cache()
    cached_landmarks = {name for name, pid in places_cache["landmarks"].items() if pid is not None}
    # Only use cached landmarks to avoid API calls
    available_landmarks = [lm for lm in all_landmarks if lm in cached_landmarks]
    if not available_landmarks:
        print("[!] places_cache.json is empty or does not exist.")
        print("    Please run place_crawler.py first: python place_crawler.py")
        return
    print(f"[Config] Target: {min_webpages} webpages | max {max_crawls} crawls | {len(available_landmarks)} landmarks (cached)")

    # Initialize aggregate db_data (shared across all crawls)
    db_data = {
        "places": {},
        "webpages": {},
        "users": {},
        "bookings": {},
        "routes": {},
        "search_along_routes": {},
        "transit_schedules": {},
        "events": {},
        "council_reports": {}
    }

    crawl_idx = 0
    while len(db_data["webpages"]) < min_webpages and crawl_idx < max_crawls:
        current_webpages = len(db_data["webpages"])
        print(f"\n{'='*60}")
        print(f"CRAWL ITERATION {crawl_idx + 1} | Webpages: {current_webpages}/{min_webpages}")
        print(f"{'='*60}")

        # --- Generate Random User ---
        user_info = random.choice(all_names)
        user_id = f"user_{crawl_idx + 1:03d}"
        user_phone = generate_australian_phone()
        db_data["users"][user_id] = {
            "user_id": user_id,
            "name": user_info["name"],
            "phone": user_phone
        }
        print(f"[User] {user_id}: {user_info['name']} ({user_phone})")

        # --- Randomly select Origin and Dest from cached list ---
        origin_query, dest_query = random.sample(available_landmarks, 2)

        print("\n--- STEP 1: READ LANDMARKS FROM CACHE ---")
        print(f"  Origin : {origin_query}")
        print(f"  Dest   : {dest_query}")

        place_ids_to_process = set()

        origin_id = places_cache["landmarks"][origin_query]
        dest_id = places_cache["landmarks"][dest_query]
        place_ids_to_process.update([origin_id, dest_id])

        origin_details = places_cache["by_id"].get(origin_id, {})
        dest_details = places_cache["by_id"].get(dest_id, {})

        if not origin_details or not dest_details:
            print("  [!] Cache missing details for landmark, skipping this iteration.")
            continue

        origin_name = origin_details.get("displayName", {}).get("text", "Origin")
        dest_name = dest_details.get("displayName", {}).get("text", "Destination")

        origin_lat = origin_details.get("location", {}).get("latitude")
        origin_lng = origin_details.get("location", {}).get("longitude")
        dest_lat = dest_details.get("location", {}).get("latitude")
        dest_lng = dest_details.get("location", {}).get("longitude")

        if None in (origin_lat, origin_lng, dest_lat, dest_lng):
            print("  [!] Missing coordinates in cache, skipping this iteration.")
            continue

        print("\n--- STEP 2: FETCH ROUTE USING COORDINATES ---")
        origin_loc = {"lat": origin_lat, "lng": origin_lng}
        dest_loc = {"lat": dest_lat, "lng": dest_lng}
        travel_mode = "DRIVE"

        route_key = f"{round(origin_lat,3)},{round(origin_lng,3)}|{round(dest_lat,3)},{round(dest_lng,3)}|{travel_mode}"
        route_data = fetch_compute_route(origin_loc, dest_loc, travel_mode)

        if route_data:
            polyline = route_data.get("polyline", {}).get("encodedPolyline", "")
            db_data["routes"][route_key] = {
                "route_key": route_key,
                "distance_meters": route_data.get("distanceMeters", 0),
                "duration_seconds": int(route_data.get("duration", "0s").replace("s", "")),
                "polyline": polyline
            }

            print("\n--- STEP 3: FIND VENUES ALONG THE ROUTE ---")
            search_along_query = "cafe"
            route_places = fetch_search_along_route(polyline, search_along_query)[:3]

            search_key = f"{polyline[:10]}_{search_along_query.lower()}"
            db_data["search_along_routes"][search_key] = route_places
            place_ids_to_process.update(route_places)

        print("\n--- STEP 4: FETCH FULL DETAILS & CRAWL WEB FOR ALL ---")
        for pid in place_ids_to_process:
            # Skip if already in current run's DB
            if pid in db_data["places"]:
                print(f"  [~] Place {pid} already in DB, skipping.")
                continue

            # Prioritize cache, only call API for new places (along route)
            raw_details = places_cache["by_id"].get(pid)
            if raw_details:
                print(f"  [cache] {pid}")
            else:
                raw_details = fetch_place_details(pid)
                if not raw_details:
                    continue
                # Save to cache for future use
                places_cache["by_id"][pid] = raw_details

            # 1. Process Reviews
            parsed_reviews = [{"rating": r.get("rating", 0), "text": r.get("text", {}).get("text", ""), "time": r.get("publishTime", "")} for r in raw_details.get("reviews", [])[:3]]

            # 2. Process Opening Hours
            opening_hours = None
            if "regularOpeningHours" in raw_details:
                opening_hours = {
                    "open_now": raw_details["regularOpeningHours"].get("openNow", False),
                    "weekday_text": raw_details["regularOpeningHours"].get("weekdayDescriptions", [])
                }

            # 3. Process Accessibility
            access_options = raw_details.get("accessibilityOptions", {})
            accessibility = {
                "wheelchairAccessibleParking": access_options.get("wheelchairAccessibleParking", False),
                "wheelchairAccessibleEntrance": access_options.get("wheelchairAccessibleEntrance", False)
            }

            # 4. Process Amenities
            amenities = {
                "allowsDogs": raw_details.get("allowsDogs", False),
                "goodForChildren": raw_details.get("goodForChildren", False),
                "servesVegetarianFood": raw_details.get("servesVegetarianFood", False),
                "outdoorSeating": raw_details.get("outdoorSeating", False)
            }

            website_url = raw_details.get("websiteUri")

            # 5. Assemble Final Object
            place_obj = {
                "place_id": raw_details.get("id"),
                "name": raw_details.get("displayName", {}).get("text", "Unknown"),
                "location": {
                    "lat": raw_details.get("location", {}).get("latitude", 0),
                    "lng": raw_details.get("location", {}).get("longitude", 0)
                },
                "address": raw_details.get("formattedAddress", ""),
                "types": raw_details.get("types", []),
                "rating": raw_details.get("rating", 0.0),
                "user_rating_count": raw_details.get("userRatingCount", 0),
                "price_level": raw_details.get("priceLevel", 1),
                "website_url": website_url,
                "phone_number": raw_details.get("nationalPhoneNumber", ""),
                "business_status": raw_details.get("businessStatus", "UNKNOWN"),
                "opening_hours": opening_hours,
                "accessibility": accessibility,
                "amenities": amenities,
                "reviews": parsed_reviews,
                "available_slots": generate_fake_slots()
            }
            db_data["places"][pid] = place_obj

            # Crawl Web if URL exists
            if website_url:
                markdown_content = fetch_webpage_content(website_url)
                if markdown_content:
                    db_data["webpages"][website_url] = {"url": website_url, "title": place_obj["name"], "content_markdown": markdown_content}

        print("\n--- STEP 5: GENERATE DYNAMIC MOCK URBAN DATA ---")
        # Attach events to Destination via ID
        generate_fake_events(db_data, [{"id": dest_id, "name": dest_name,
                                        "lat": dest_lat, "lng": dest_lng}])
        # Attach bus schedule to Origin via ID
        generate_fake_transit(db_data, [{"id": origin_id, "name": origin_name,
                                         "dest_name": dest_name,
                                         "lat": origin_lat, "lng": origin_lng}])

        crawl_idx += 1
        print(f"  [Progress] Valid webpages: {len(db_data['webpages'])}/{min_webpages}")

    if len(db_data["webpages"]) < min_webpages:
        print(f"\n[!] Reached limit of {max_crawls} iterations with only {len(db_data['webpages'])} webpages (target: {min_webpages}).")
        print("    Try increasing MAX_CRAWLS or running place_crawler.py to expand the pool.")
    else:
        print(f"\n[OK] Target of {min_webpages} webpages reached after {crawl_idx} iterations.")

    # Save cache (add newly discovered places along route)
    save_places_cache(places_cache)

    # Export accumulated data to db.json
    with open(f"db_{CITY}.json", "w", encoding="utf-8") as f:
        json.dump(db_data, f, indent=4, ensure_ascii=False)
    print(f"[OK] Saved to db_{CITY}.json | places: {len(db_data['places'])} | webpages: {len(db_data['webpages'])} | users: {len(db_data['users'])}")

if __name__ == "__main__":
    build_database(MIN_WEBPAGES, MAX_CRAWLS)