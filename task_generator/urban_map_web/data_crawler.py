def fetch_text_search(query: str, max_results: int = 3) -> list:
    """Free-text search for places to get Place IDs."""
    print(f"[*] Searching for: '{query}'...")
    url = "https://places.googleapis.com/v1/places:searchText"
    headers = {
        "Content-Type": "application/json",
        "X-Goog-Api-Key": API_KEY,
        "X-Goog-FieldMask": "places.id" # Only fetch ID to save payload size
    }
    payload = {
        "textQuery": query,
        "languageCode": "en",
        "maxResultCount": max_results
    }
    
    response = requests.post(url, json=payload, headers=headers)
    if response.status_code == 200:
        places = response.json().get("places", [])
        print(f"  -> Found {len(places)} places.")
        return [p["id"] for p in places]
    else:
        print(f"  [!] Text Search Error: {response.text}")
        return []

def fetch_compute_route(origin: dict, dest: dict, travel_mode: str = "DRIVE") -> dict:
    """Calculate routes and retrieve the actual polyline."""
    print(f"[*] Fetching route from {origin} to {dest}...")
    url = "https://routes.googleapis.com/directions/v2:computeRoutes"
    headers = {
        "Content-Type": "application/json",
        "X-Goog-Api-Key": API_KEY,
        "X-Goog-FieldMask": "routes.distanceMeters,routes.duration,routes.polyline.encodedPolyline"
    }
    payload = {
        "origin": {"location": {"latLng": {"latitude": origin["lat"], "longitude": origin["lng"]}}},
        "destination": {"location": {"latLng": {"latitude": dest["lat"], "longitude": dest["lng"]}}},
        "travelMode": travel_mode
    }
    
    response = requests.post(url, json=payload, headers=headers)
    if response.status_code == 200:
        routes = response.json().get("routes", [])
        if routes:
            print("  -> Route retrieved successfully.")
            return routes[0]
    print(f"  [!] Error fetching route: {response.text}")
    return {}

def fetch_search_along_route(polyline: str, query: str) -> list:
    """Find places (place_ids) along the Polyline."""
    print(f"[*] Searching for '{query}' along the route...")
    url = "https://places.googleapis.com/v1/places:searchText"
    headers = {
        "Content-Type": "application/json",
        "X-Goog-Api-Key": API_KEY,
        "X-Goog-FieldMask": "places.id"
    }
    payload = {
        "textQuery": query,
        "searchAlongRouteParameters": {
            "polyline": {"encodedPolyline": polyline}
        }
    }
    
    response = requests.post(url, json=payload, headers=headers)
    if response.status_code == 200:
        places = response.json().get("places", [])
        print(f"  -> Found {len(places)} places along the route.")
        return [p["id"] for p in places]
    print(f"  [!] Search Along Route Error: {response.text}")
    return []

