# Jina patterns for non-existent pages / errors
_WEBPAGE_INVALID_PREFIXES = (
    "not found",
    "404",
    "page not found",
    "error",
    "access denied",
    "403 forbidden",
    "this site can\u2019t be reached",
    "this page isn",
)

def _is_valid_webpage(content: str) -> bool:
    """Returns False if content is an error / not found page."""
    if not content or len(content.strip()) < 100:
        return False
    first_line = content.strip().splitlines()[0].lower()
    return not any(first_line.startswith(p) for p in _WEBPAGE_INVALID_PREFIXES)

def fetch_webpage_content(url: str) -> str:
    """Use Jina Reader to crawl Markdown from the venue's website."""
    print(f"[*] Crawling website content: {url}...")
    jina_url = f"https://r.jina.ai/{url}"
    try:
        response = requests.get(jina_url, timeout=20)
        if response.status_code == 200:
            content = response.text[:4000]
            if not _is_valid_webpage(content):
                print("  [~] Invalid content (Not found / error), skipping.")
                return ""
            print("  -> Success.")
            return content
        else:
            print(f"  [!] Web crawl error: HTTP {response.status_code}")
            return ""
    except Exception as e:
        print(f"  [!] Web crawl Timeout/Connection error: {e}")
        return ""

