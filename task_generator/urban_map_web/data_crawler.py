def _fetch_ticketmaster_events(lat: float, lng: float) -> list:
    """Fetch real events from Ticketmaster Discovery API if key is available."""
    if not TICKETMASTER_API_KEY:
        return []
    url = "https://app.ticketmaster.com/discovery/v2/events.json"
    params = {
        "apikey": TICKETMASTER_API_KEY,
        "latlong": f"{lat},{lng}",
        "radius": "5",
        "unit": "km",
        "size": "5",
        "sort": "date,asc",
        "countryCode": "AU"
    }
    try:
        resp = requests.get(url, params=params, timeout=10)
        if resp.status_code == 200:
            items = resp.json().get("_embedded", {}).get("events", [])
            results = []
            for ev in items:
                date_info = ev.get("dates", {}).get("start", {})
                results.append({
                    "event_name": ev.get("name", "Event"),
                    "date": date_info.get("localDate", ""),
                    "time": date_info.get("localTime", "19:00")[:5],
                    "ticket_price": ev.get("priceRanges", [{}])[0].get("min", 0.0) if ev.get("priceRanges") else 0.0,
                    "description": ev.get("info") or ev.get("pleaseNote") or f"Event at Melbourne: {ev.get('name', '')}",
                    "url": ev.get("url", ""),
                    "source": "Ticketmaster"
                })
            if results:
                print(f"  -> Fetched {len(results)} real events from Ticketmaster.")
                return results
    except Exception as e:
        print(f"  [~] Ticketmaster API error: {e}, falling back to fake data.")
    return []


